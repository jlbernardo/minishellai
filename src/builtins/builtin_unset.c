#include "minishell.h"

int	builtin_unset(t_shell *shell, t_cmd *cmd)
{
	int	i;

	i = 1;
	while (i < cmd->argc)
	{
		unset_env_value(&shell->env, cmd->args[i]);
		i++;
	}
	return (0);
}
